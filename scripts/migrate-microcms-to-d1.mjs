/*
 * microCMS → Cloudflare D1 一回限りのデータ移行スクリプト
 *
 * 拠点別 microCMS サービス（daikanyama / okinawa）の books / history を全件取得し、
 * D1 用の seed.sql を stdout に出力する。進捗・件数サマリは stderr に出力する。
 *
 * 必要な環境変数（どちらか一方の拠点だけでも可。両方未設定はエラー）:
 *   MICROCMS_DAIKANYAMA_API_KEY
 *   MICROCMS_DAIKANYAMA_BASE_URL   例: https://xxx.microcms.io/api/v1
 *   MICROCMS_OKINAWA_API_KEY
 *   MICROCMS_OKINAWA_BASE_URL
 *
 * 実行例:
 *   node scripts/migrate-microcms-to-d1.mjs > seed.sql && \
 *     npx wrangler d1 execute bookbook-db --remote --file=seed.sql
 */

const LIMIT = 100;

function buildLocations() {
  const defs = [
    {
      name: "daikanyama",
      apiKey: process.env.MICROCMS_DAIKANYAMA_API_KEY,
      baseUrl: process.env.MICROCMS_DAIKANYAMA_BASE_URL,
    },
    {
      name: "okinawa",
      apiKey: process.env.MICROCMS_OKINAWA_API_KEY,
      baseUrl: process.env.MICROCMS_OKINAWA_BASE_URL,
    },
  ];
  return defs.filter((d) => d.apiKey && d.baseUrl);
}

async function fetchAllContents(location, endpoint, extraQuery = "") {
  const all = [];
  let offset = 0;
  while (true) {
    const url = `${location.baseUrl}/${endpoint}?limit=${LIMIT}&offset=${offset}${extraQuery}`;
    const res = await fetch(url, {
      headers: { "X-MICROCMS-API-KEY": location.apiKey },
    });
    if (!res.ok) {
      const body = await res.text();
      console.error(
        `[${location.name}] GET ${endpoint} failed: ${res.status} ${res.statusText}\n${body}`,
      );
      process.exit(1);
    }
    const json = await res.json();
    all.push(...json.contents);
    offset += json.contents.length;
    console.error(
      `[${location.name}] ${endpoint}: fetched ${offset}/${json.totalCount}`,
    );
    if (offset >= json.totalCount || json.contents.length === 0) break;
  }
  return all;
}

function sqlString(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlNumber(value) {
  if (value === null || value === undefined) return "NULL";
  const n = Number(value);
  return Number.isFinite(n) ? String(n) : "NULL";
}

function bookInsert(book, locationName) {
  const values = [
    sqlString(book.id),
    sqlString(locationName),
    sqlString(book.title),
    sqlString(book.author),
    sqlString(book.publisher),
    sqlString(book.description),
    sqlString(book.published_date),
    sqlNumber(book.page_count),
    sqlString(book.cover_metadata?.src),
    sqlNumber(book.total),
    sqlNumber(book.available_count),
    sqlString(book.createdAt),
    sqlString(book.updatedAt),
  ].join(", ");
  return `INSERT INTO books (isbn, location, title, author, publisher, description, published_date, page_count, cover_src, total, available_count, created_at, updated_at) VALUES (${values});`;
}

function historyInsert(history, locationName) {
  const isbn = sqlString(history.isbn);
  const location = sqlString(locationName);
  const bookIdSubquery = `(SELECT id FROM books WHERE isbn = ${isbn} AND location = ${location})`;
  const values = [
    bookIdSubquery,
    isbn,
    location,
    sqlString(history.borrower_email ?? ""),
    sqlString(history.borrower_name),
    sqlString(history.checkout_date ?? history.createdAt),
    sqlString(history.return_date),
    history.is_done ? 1 : 0,
  ].join(", ");
  return `INSERT INTO histories (book_id, isbn, location, borrower_email, borrower_name, checkout_date, return_date, is_done) VALUES (${values});`;
}

async function main() {
  const locations = buildLocations();
  if (locations.length === 0) {
    console.error(
      "エラー: microCMS の環境変数が未設定です。少なくとも 1 拠点分の API_KEY と BASE_URL を設定してください。",
    );
    process.exit(1);
  }

  const lines = [
    "-- Generated by scripts/migrate-microcms-to-d1.mjs",
    "-- 再実行時は全削除して入れ直す（冪等性のため）",
    "-- BEGIN/COMMIT では囲まない: wrangler d1 execute はファイル全体を暗黙にトランザクション扱いするため",
    "DELETE FROM histories;",
    "DELETE FROM books;",
    "",
  ];

  const summary = [];
  const historyLines = [];

  for (const location of locations) {
    const books = await fetchAllContents(location, "books");
    for (const book of books) {
      lines.push(bookInsert(book, location.name));
    }
    const histories = await fetchAllContents(location, "history", "&depth=2");
    for (const history of histories) {
      historyLines.push(historyInsert(history, location.name));
    }
    summary.push(
      `[${location.name}] books: ${books.length}, histories: ${histories.length}`,
    );
  }

  lines.push("", ...historyLines, "");
  process.stdout.write(lines.join("\n"));

  console.error("--- summary ---");
  for (const s of summary) console.error(s);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
